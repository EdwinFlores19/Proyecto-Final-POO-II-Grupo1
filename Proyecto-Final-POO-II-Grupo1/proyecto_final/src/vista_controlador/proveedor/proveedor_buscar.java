    private void ruc_KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ruc_KeyPressed
        char c = evt.getKeyChar();
        if (Character.isLetter(c)) {
            ruc_.setEditable(false);
        } else {
            ruc_.setEditable(true);
        }
    }//GEN-LAST:event_ruc_KeyPressed

    private void b_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_buscarActionPerformed
        String ruc = ruc_.getText();

        Proveedor p1 = proveedor_a.busqueda_proveedor(ruc);
        if (p1.getRuc()!="0") {
            ruc__.setText(p1.getRuc());
            razon_.setText(p1.getRaz_social());
            direccion_.setText(p1.getDireccion());
            contacto1_.setText(p1.getNom_contact1());
            contacto2_.setText(p1.getNom_contact2());
            website_.setText(p1.getWebsite());
            sucursal_.setText(p1.getSurcursal());

            ruc_.setText("");

        } else {
            ruc__.setText("");
            razon_.setText("");
            direccion_.setText("");
            contacto1_.setText("");
            contacto2_.setText("");
            website_.setText("");
            sucursal_.setText("");
            ruc_.setText("");
            JOptionPane.showMessageDialog(null, "No existe un proveedor con el RUC ingresado", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_b_buscarActionPerformed

